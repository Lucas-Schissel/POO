    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
       DBController db = new DBController("jogo_luta.db");
        if(!db.conectar()){
            JOptionPane.showMessageDialog(null,"Arquivo n√£o encontrado");
            return;
        }
        
        String nome = edt_nome.getText();
        String nacionalidade = edt_nacionalidade.getText();
        String categoria = (String) box_categoria.getSelectedItem();        
        int vitorias = 0;
        int derrotas = 0;
        int empates = 0;       
        
        if(nome.trim().length() == 0 || nacionalidade.trim().length() == 0){
            JOptionPane.showMessageDialog(null, "Campos devem ser preenchidos");
            return;
        }
        
        if(!db.cadastrar(nome,nacionalidade,categoria,vitorias,derrotas,empates)){
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar Lutador: ");
            return;
        }
        
        JOptionPane.showMessageDialog(null, "Lutador cadastrado com sucesso!");
        
        db.desconectar();
        
        new Luta_Extrema2().setVisible(true);
        dispose();
    }//GEN-LAST:event_btn_SalvarActionPerformed

