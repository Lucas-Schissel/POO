    private void btn_gerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_gerarActionPerformed
        
        DBController db = new DBController("jogo_luta.db");
        if(!db.conectar()){
            JOptionPane.showMessageDialog(null,"Arquivo nรฃo encontrado");
            return;
        }
        
        int qtd = db.quantidade();
                            
         Random ale1 = new Random();
         Random ale2 = new Random();
         int valor1 = ale1.nextInt(qtd);
         int valor2 = ale2.nextInt(qtd);
         
         
         String lutador1 = db.lutador1(valor1,2);
         c1 = lutador1;
         String nac1 = db.lutador1(valor1,3);
         b1 = nac1;
         String cat1 = db.lutador1(valor1,4);
         a1 = cat1;
         
         
         String lutador2 = db.lutador2(valor2,2);
         c2 = lutador2;
         String nac2 = db.lutador2(valor2,3);
         b2 = nac2;
         String cat2 = db.lutador2(valor2,4);
         a2 = cat2;
         
         edt_lutador1.setText(lutador1);
         edt_lutador2.setText(lutador2);
         edt_cat1.setText(a1);
         edt_cat2.setText(a2);
         edt_nac1.setText(b1);
         edt_nac2.setText(b2);
       
         
         db.desconectar();
    }//GEN-LAST:event_btn_gerarActionPerformed

    private void btn_lutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_lutarActionPerformed
        DBController db = new DBController("jogo_luta.db");
        if(!db.conectar()){
            JOptionPane.showMessageDialog(null,"Arquivo nรฃo encontrado");
            return;
        }
        
        if( a1.equals(a2) ){

            Random ale = new Random();
            int resultado = ale.nextInt(3);
            switch(resultado){
                case 0:
                    JOptionPane.showMessageDialog(null,"Luta Empatou!");
                    
                    db.lutaempatar(1,c1);
                    db.lutaempatar(1,c2);
                    
                    break;
                    
                case 1:
                    JOptionPane.showMessageDialog(null,"Lutador: " + c1 + " ganhou!");
                    
                    db.lutaganha(1,c1);
                    db.lutaperder(1,c2);
                    
                    break;
                case 2:
                    JOptionPane.showMessageDialog(null,"Lutador: " + c2 + " ganhou!");
                    
                    db.lutaganha(1,c2);
                    db.lutaperder(1,c1);
                    
                    break;
                    
            }
         
        }else{
            JOptionPane.showMessageDialog(null,"Categorias Incompativeis!");
        }
        
        db.desconectar();
    }//GEN-LAST:event_btn_lutarActionPerformed
    
